int key_count=0;
#include"time_clock.h"


int main(){
	char key[7],temp_key;
	int _h,_m,_s;
	display_time(20,59,55);		
	while(1){
		key[key_count]=get_key();
		key_shake_eliminate();
		if(key[key_count]!=-1){
			key_count++;
			temp_key=get_key();
			key_shake_eliminate();
			if(key_count==6) break;
			key_count%=6;
		}				
		led_display_puts(key);
		led_display_scan();
	}
	_h=(key[0]-'0')*10+key[1]-'0';
	_m=(key[2]-'0')*10+key[3]-'0';
	_s=(key[4]-'0')*10+key[5]-'0';
	display_time(_h,_m,_s);
	
}
